const Notification = ({ message, type }) => {
  if (message === null) {
    return null;
  }

  // type "error" или "success"
  const style = {
    color: "black",
    //background: "lightgrey",
    fontSize: 16,
    borderColor: type === "error" ? "red" : "green",
    borderStyle: "solid",
    borderRadius: 5,
    padding: 10,
    marginBottom: 10,
  };

  return <div style={style}>{message}</div>;
};

export default Notification;
